public class Ex321 {
    public static void main(String[] args) {
        System.out.println("7+2=%2d".formatted(7+2));
        System.out.println("7-2=%2d".formatted(7-2));
        System.out.println("7x2=%2d".formatted(7*2));
        System.out.println("7/2=%2d".formatted(7/2));
        System.out.println("7%2="
                + 7%2);
    }
}
